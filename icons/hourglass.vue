                fill="none"
                :height="size"
                :stroke="color"
                stroke-linecap="round"
                stroke-linejoin="round"
                :stroke-width="strokeWidth"
                viewBox="0 0 24 24"
                :width="size"
              
            :class="{ animating: isAnimating || active }">
                <g}},
                    animate: {
                      rotate: 180,
                    },
                  }}
                >
                  <path d="M5 22h14" />
                  <path d="M5 2h14" />
